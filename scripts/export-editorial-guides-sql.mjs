import fs from "fs";
import path from "path";
import vm from "vm";
import ts from "typescript";

const ROOT = process.cwd();
const USERS_PATH = path.join(ROOT, "src/data/users.ts");
const LISTS_PATH = path.join(ROOT, "src/data/lists.ts");
const OUTPUT_PATH = path.join(ROOT, "supabase/editorial-guides.sql");

function transpileTs(filePath) {
  const source = fs.readFileSync(filePath, "utf8");
  const result = ts.transpileModule(source, {
    compilerOptions: {
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2020,
      esModuleInterop: true,
    },
    fileName: filePath,
  });
  return result.outputText;
}

function runCommonJs(code, filePath, requireImpl) {
  const module = { exports: {} };
  const context = vm.createContext({
    console,
    exports: module.exports,
    module,
    require: requireImpl,
    __filename: filePath,
    __dirname: path.dirname(filePath),
  });
  vm.runInContext(code, context, { filename: filePath });
  return module.exports;
}

const usersExports = runCommonJs(transpileTs(USERS_PATH), USERS_PATH, (specifier) => {
  throw new Error(`Unexpected require from users.ts: ${specifier}`);
});

const listsExports = runCommonJs(transpileTs(LISTS_PATH), LISTS_PATH, (specifier) => {
  if (specifier === "@/data/users") {
    return usersExports;
  }
  throw new Error(`Unexpected require from lists.ts: ${specifier}`);
});

const mapLists = listsExports.mapLists;
if (!Array.isArray(mapLists)) {
  throw new Error("Expected src/data/lists.ts to export mapLists.");
}

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

const rows = mapLists
  .map((list) => {
    const json = JSON.stringify(list).replace(/'/g, "''");
    return `  (${sqlString(list.id)}, ${sqlString(list.slug)}, ${sqlString(list.category)}, ${sqlString(list.location.country)}, ${list.location.city ? sqlString(list.location.city) : "null"}, ${list.location.neighborhood ? sqlString(list.location.neighborhood) : "null"}, '${json}'::jsonb)`;
  })
  .join(",\n");

const sql = `-- Generated by scripts/export-editorial-guides-sql.mjs
-- Canonical seed for R Guide editorial lists.

create table if not exists public.editorial_guides (
  id text primary key,
  slug text not null unique,
  category text not null,
  country text not null,
  city text,
  neighborhood text,
  list jsonb not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.editorial_guides enable row level security;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists editorial_guides_set_updated_at on public.editorial_guides;

create trigger editorial_guides_set_updated_at
before update on public.editorial_guides
for each row
execute function public.set_updated_at();

drop policy if exists "Editorial guides are readable" on public.editorial_guides;

create policy "Editorial guides are readable"
on public.editorial_guides
for select
using (true);

create index if not exists editorial_guides_category_idx on public.editorial_guides (category);
create index if not exists editorial_guides_country_city_idx on public.editorial_guides (country, city);
create index if not exists editorial_guides_neighborhood_idx on public.editorial_guides (neighborhood);
create index if not exists editorial_guides_list_gin_idx on public.editorial_guides using gin (list);

insert into public.editorial_guides (
  id,
  slug,
  category,
  country,
  city,
  neighborhood,
  list
)
values
${rows}
on conflict (id) do update set
  slug = excluded.slug,
  category = excluded.category,
  country = excluded.country,
  city = excluded.city,
  neighborhood = excluded.neighborhood,
  list = excluded.list;
`;

fs.writeFileSync(OUTPUT_PATH, sql);
console.log(`Exported ${mapLists.length} editorial guides to ${path.relative(ROOT, OUTPUT_PATH)}`);
